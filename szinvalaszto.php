<?php
    function Szinvalaszto($titkos) {
        if ($titkos == "piros") {
            return "#FF0000";
        }
        else if($titkos == "zold") {
            return "#8FCE00";
        }
        else if($titkos == "sarga") {
            return "#F3FF00";
        }
        else if($titkos == "kek") {
            return "#0091FF";
        }
        else if($titkos == "fekete") {
            return "#000000";
        }
        else if($titkos == "feher") {
            return "#FFFFFF";
        }
    }
?>